"use strict";
//Exemplo 01 - Single Quotes
let nomeCompleto = 'Isaque Matarazzo'; // String simples
console.log(nomeCompleto); // Isaque Matarazzo
//Exemplo 02 - Double Quotes
let funcaoEmpresa = "Desenvolvedor de Software"; // String com aspas duplas
console.log(funcaoEmpresa); // Desenvolvedor de Software
//Exemplo 03 - Backticks
let nomeEmpresa = 'Microsoft';
let dadosFuncionaario = `Seja bem vindo, ${nomeCompleto}!, você é um(a) ${funcaoEmpresa} na ${nomeEmpresa}.`;
console.log(dadosFuncionaario); // Seja bem vindo, Isaque Matarazzo!, você é um(a) Desenvolvedor de Software na Microsoft.
